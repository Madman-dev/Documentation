 ㄴ tab bar controllers are key to navigating between view controllers allowing you to comfortably pack more functionality into a single app
 
 
 ⭐️ - 탭 바는 네비게이션 hierarchy가 따로 있어서 view들 간의 이동뿐만 아니라 hierarchy 자체를 늘려줄 수 있도록 만드는 것 같다.
 
 - 탭 바 네비게이션을 적용하면 하단에 네비게이션이 별도로 생기는구나
